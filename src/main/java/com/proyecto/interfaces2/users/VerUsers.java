    private void btnModificarUMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarUMouseClicked
        String id;
        id=jTextField1.getText();
        if(id.length()!=0){
            Usuario usuario1 = usuarioDAO.buscar(Integer.parseInt(id));
            if(usuario1!=null){
            Modificar mod = new Modificar(usuario, usuario1);
            mod.setVisible(true);
            this.setVisible(false);
            }else{
                showMessageDialog(null, "No se encontro");
            }  
        }else{
            showMessageDialog(null, "Ingrese una ID");
        }
        
    }//GEN-LAST:event_btnModificarUMouseClicked

